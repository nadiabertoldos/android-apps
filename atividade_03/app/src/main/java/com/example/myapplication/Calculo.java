package com.example.myapplication;

public class Calculo {

    public  int  getSubtracao(int a, int b)
    {
        int c = a - b;
        return c;
    }

    public int getDivisao(int a, int b){
        int c = a / b;
        return c;
    }

    public int getMultiplicar(int a, int b){
        int c = a * b;
        return c;
    }
}
